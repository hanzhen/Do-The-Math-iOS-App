#import "Solve+Functions.h"

@implementation Solve (Functions)

/**
 * Gives a description of the Correct-Wrong object
 */

- (NSString *)description {
    NSString *description = [NSString stringWithFormat:@"Question:[id:%@,type:%@,question:%@,answer:%@,yourAnswer:%@,theirAnswer:%@, yourtimeSpent: %d, theirtimeSpent: %d] ",self.questionId,[self class],self.question,self.answer,self.yourAnswer,self.theirAnswer,  [self.yourTimeSpent intValue], [self.theirTimeSpent intValue]];
    return description;
}



@end
